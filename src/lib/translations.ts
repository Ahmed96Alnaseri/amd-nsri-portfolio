/** Resolve a dotted key path (e.g. "hero.tagline") for a language. */
export function translate(lang: Lang, key: string): string {
  const dig = (root: unknown): unknown =>
    key.split('.').reduce<unknown>(
      (acc, part) => (acc && typeof acc === 'object' ? (acc as Record<string, unknown>)[part] : undefined),
      root,
    );
  const val = dig(translations[lang]);
  if (typeof val === 'string') return val;
  const fallback = dig(translations.EN);
  return typeof fallback === 'string' ? fallback : key;
}

/** Translate a data-file value by its English string; passthrough if unmapped. */
export function translateValue(lang: Lang, value: string): string {
  if (lang === 'EN') return value;
  return VALUES[lang][value] ?? value;
}
